package parttern.gcz;

public class Templater {
	private int kq;
	private int wd;
	private int zl;
	
	public Templater(int kq,int wd,int zl) {
		this.kq = kq;
		this.wd = wd;
		this.zl = zl;
	}

	public int getKq() {
		return kq;
	}

	public void setKq(int kq) {
		this.kq = kq;
	}

	public int getWd() {
		return wd;
	}

	public void setWd(int wd) {
		this.wd = wd;
	}

	public int getZl() {
		return zl;
	}

	public void setZl(int zl) {
		this.zl = zl;
	}
	
	
}
